export type I18nKey = keyof typeof fr;

function systemLanguage(): Lang {
  if (typeof navigator === "undefined") return "en";
  const code = navigator.language.toLowerCase();
  if (code.startsWith("fr")) return "fr";
  if (code.startsWith("en")) return "en";
  return "en";
}

function readStoredLanguage(): LanguageSetting {
  try {
    const raw = JSON.parse(localStorage.getItem(SETTINGS_KEY) ?? "{}");
    return raw.language === "fr" || raw.language === "en" || raw.language === "system"
      ? raw.language
      : "system";
  } catch {
    return "system";
  }
}

function resolveLanguage(setting: LanguageSetting): Lang {
  return setting === "system" ? systemLanguage() : setting;
}

let languageSetting: LanguageSetting = readStoredLanguage();
let currentLanguage: Lang = resolveLanguage(languageSetting);

export function getLanguageSetting(): LanguageSetting {
  return languageSetting;
}

export function getResolvedLanguage(): Lang {
  return currentLanguage;
}

export function setLanguage(next: LanguageSetting) {
  languageSetting = next;
  currentLanguage = resolveLanguage(next);
  window.dispatchEvent(new CustomEvent(LANGUAGE_CHANGED_EVENT, {
    detail: { setting: languageSetting, language: currentLanguage },
  }));
}

export function t(key: I18nKey, vars?: Record<string, string | number | null | undefined>): string {
  const dict = currentLanguage === "en" ? en : fr;
  const template = dict[key] ?? fr[key] ?? key;
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? ""));
}

export function eventLabel(name: string): string {
  if (name === "__thinking") return t("event.thinking");
  if (name === "__compacted") return t("event.compacted");
  if (name === "__queued") return t("event.queued");
  if (name === "__steered") return t("event.steered");
  if (name === "__permission-fallback") return t("event.permission-fallback");
  if (name === "__session-cleared") return t("event.session-cleared");
  if (name.startsWith("__edits:")) {
    const files = name.slice("__edits:".length).trim();
    return files ? t("event.edits", { files }) : t("event.edits-files");
  }
  if (name.startsWith("__provider-switch:")) {
    return t("event.provider-switch", { provider: name.slice("__provider-switch:".length) });
  }
  return name;
}
